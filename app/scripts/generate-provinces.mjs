import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const svgPath = resolve(here, "../public/philippines.svg");
const tsOut = resolve(here, "../src/lib/geo/ph-provinces.ts");
const jsonOut = resolve(here, "../../api/database/seeders/data/provinces.json");

const svg = readFileSync(svgPath, "utf8");

const width = Number(/width="([\d.]+)"/.exec(svg)[1]);
const height = Number(/height="([\d.]+)"/.exec(svg)[1]);
const geo = /geoViewBox="([\d.]+) ([\d.]+) ([\d.]+) ([\d.]+)"/.exec(svg);
const west = Number(geo[1]);
const north = Number(geo[2]);
const east = Number(geo[3]);
const south = Number(geo[4]);

function normalizeCode(id) {
  return id.replace(/_/g, "-").toUpperCase();
}

const round = (n) => Math.round(n * 10) / 10;

// Walk an SVG path, returning its bbox plus simplified polygon rings (one per
// subpath) for point-in-polygon hit testing.
function parsePath(d) {
  const tokens = d.match(/[a-zA-Z]|-?\d*\.?\d+(?:e-?\d+)?/g) || [];
  let i = 0;
  let cx = 0;
  let cy = 0;
  let sx = 0;
  let sy = 0;
  let started = false;
  let cmd = null;
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;
  const rings = [];
  let ring = null;
  const mark = (x, y) => {
    if (x < minX) minX = x;
    if (y < minY) minY = y;
    if (x > maxX) maxX = x;
    if (y > maxY) maxY = y;
  };
  const push = (x, y) => {
    if (!ring) return;
    const n = ring.length;
    if (n >= 2 && Math.hypot(x - ring[n - 2], y - ring[n - 1]) < 1.5) return;
    ring.push(round(x), round(y));
  };
  const num = () => Number(tokens[i++]);
  while (i < tokens.length) {
    const t = tokens[i];
    if (/[a-zA-Z]/.test(t)) {
      cmd = t;
      i++;
    }
    if (cmd == null) {
      i++;
      continue;
    }
    const rel = cmd === cmd.toLowerCase();
    const C = cmd.toUpperCase();
    if (C === "M") {
      let x = num();
      let y = num();
      if (rel && started) {
        x += cx;
        y += cy;
      }
      cx = x;
      cy = y;
      sx = x;
      sy = y;
      started = true;
      ring = [];
      rings.push(ring);
      push(cx, cy);
      mark(cx, cy);
      cmd = rel ? "l" : "L";
    } else if (C === "L") {
      let x = num();
      let y = num();
      if (rel) {
        x += cx;
        y += cy;
      }
      cx = x;
      cy = y;
      push(cx, cy);
      mark(cx, cy);
    } else if (C === "H") {
      let x = num();
      if (rel) x += cx;
      cx = x;
      push(cx, cy);
      mark(cx, cy);
    } else if (C === "V") {
      let y = num();
      if (rel) y += cy;
      cy = y;
      push(cx, cy);
      mark(cx, cy);
    } else if (C === "C") {
      num();
      num();
      num();
      num();
      let x = num();
      let y = num();
      if (rel) {
        x += cx;
        y += cy;
      }
      cx = x;
      cy = y;
      push(cx, cy);
      mark(cx, cy);
    } else if (C === "Z") {
      cx = sx;
      cy = sy;
    } else {
      i++;
    }
  }
  return { minX, minY, maxX, maxY, rings: rings.filter((r) => r.length >= 6) };
}

const pathRe = /<path\s+id="([^"]+)"\s+title="([^"]*)"\s+d="([^"]+)"\s*\/>/g;

const provinces = [];
const seed = [];
let m;
while ((m = pathRe.exec(svg)) !== null) {
  const code = normalizeCode(m[1]);
  const title = m[2];
  const d = m[3];

  const parsed = parsePath(d);
  provinces.push({
    code,
    title,
    d,
    bbox: [
      round(parsed.minX),
      round(parsed.minY),
      round(parsed.maxX),
      round(parsed.maxY),
    ],
    polys: parsed.rings,
  });

  const midX = (parsed.minX + parsed.maxX) / 2;
  const midY = (parsed.minY + parsed.maxY) / 2;
  const lng = west + (midX / width) * (east - west);
  const lat = north + (midY / height) * (south - north);
  seed.push({
    code,
    name: title,
    latitude: Number(lat.toFixed(4)),
    longitude: Number(lng.toFixed(4)),
  });
}

const tsBody = `// GENERATED by scripts/generate-provinces.mjs from public/philippines.svg. Do not edit by hand.
export const PH_VIEWBOX = {
  width: ${width},
  height: ${height},
  geo: { west: ${west}, north: ${north}, east: ${east}, south: ${south} },
} as const;

export type ProvincePath = {
  code: string;
  title: string;
  d: string;
  bbox: [number, number, number, number];
  polys: number[][];
};

export const PH_PROVINCES: ProvincePath[] = ${JSON.stringify(provinces)};
`;

mkdirSync(dirname(tsOut), { recursive: true });
mkdirSync(dirname(jsonOut), { recursive: true });
writeFileSync(tsOut, tsBody);
writeFileSync(jsonOut, JSON.stringify(seed, null, 2));

console.log(`provinces: ${provinces.length}`);
console.log(`ts  -> ${tsOut} (${(tsBody.length / 1024).toFixed(0)} KB)`);
console.log(`json-> ${jsonOut}`);
